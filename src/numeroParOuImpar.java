public class numeroParOuImpar {
    public static void main(String[] arg){
        int numeroA = 8;
        int numeroB = 7;
        if (numeroA == 8){
            System.out.println("O número " + numeroA + "é par.");
        } else if (numeroB == 7){
            System.out.println("O número " + numeroB + "é impar.");
        }
    }
}